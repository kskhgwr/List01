// 読み込んだ二つの整数値の和（加算結果）を変数に格納して表示

#include<stdio.h>

int main(void)
{
    int n1, n2;

    puts("二つの整数を入力してください");
    printf("整数n1：");    scanf_s("%d", &n1);
    printf("整数n2：");    scanf_s("%d", &n2);

    int wa = n1 + n2;

    printf("それらの和は%dです。\n", wa);    // 和を表示
    printf("すなわちn1 + n2 = %dです。\n", wa);    // 和を表示

    return 0;
}